class area_of_triangle_and_rectangle {

    public static void main(String args[]) {
        int base = 10;
        int height = 12;
        int width = 20;
        int length = 30;

        double area_of_rectangle, area_of_trinagle;

        area_of_trinagle = (base * height) * 2;
        System.out.println("area of given trinagle is " + area_of_trinagle);

        area_of_rectangle = width * length;

        System.out.println("area of given rectangle is " + area_of_rectangle);

    }

}
